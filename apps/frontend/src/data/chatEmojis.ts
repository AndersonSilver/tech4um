export const RECENT_EMOJIS_KEY = "tech4um-recent-emojis";
export const MAX_RECENT_EMOJIS = 24;

export function loadRecentEmojis(): string[] {
  try {
    const raw = localStorage.getItem(RECENT_EMOJIS_KEY);
    if (!raw) return [];
    const parsed = JSON.parse(raw);
    return Array.isArray(parsed) ? parsed.filter((item) => typeof item === "string") : [];
  } catch {
    return [];
  }
}

export function saveRecentEmoji(emoji: string) {
  const recent = loadRecentEmojis().filter((item) => item !== emoji);
  recent.unshift(emoji);
  localStorage.setItem(RECENT_EMOJIS_KEY, JSON.stringify(recent.slice(0, MAX_RECENT_EMOJIS)));
}
